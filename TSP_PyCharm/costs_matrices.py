def generate_random_cost_matrix(n_vertices):
    costs_matrix = []
    for i in range(n_vertices):
        row = [0] * n_vertices
        for j in range(n_vertices):
            if i != j:
                row[j] = random.randint(1, 100)
        costs_matrix.append(row)
    return costs_matrix


